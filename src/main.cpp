#include <Arduino.h>
#include "defines.h"
#include "comandi.h"
#include "altre.h"
#include "timerAVR.h"

TimerAVR timer1;

void setup() 
{
    setupMotors();
    setupUltra();

    Serial.begin (9600);
    Serial.println ("Sensore Ultrasuoni: ");
}

void loop() 
{
    int distanceCM = DistanceUltra ();
    Serial.print(distanceCM);
    Serial.println ("cm");

    if (distanceCM < 17){
        motorBreak_ALL(70);
        motorFWD(LEFT, 70);
        delay(360); // Tempo per girare di 90°
    } 

    else 
    {
        motorFWD_ALL(70, 2);
        timer1.TON(true, 3500);
    }

    if (timer1.Q)
    {
        motorStop_ALL();
        motorFWD(LEFT, 70);
        delay(360); // Tempo per girare di 90°
        timer1.reset();
    }
    
}
